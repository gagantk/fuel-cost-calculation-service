package com.gagantk.fuelcostcalculator.model;

public class FuelPrice {

	private String city;
	private float price;

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

}
